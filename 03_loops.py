def main():
    # range is creating a list of numbers 0 - 9
    # the loop will assign the value to i each iteration of the loop
    for i in range(10):
        print(f"i is {i}")

    fruits = ["apple", "banana", "carrot"]
    print()
    for fruit in fruits:
        print(f"the fruit is {fruit}")

if __name__ == '__main__':
    main()